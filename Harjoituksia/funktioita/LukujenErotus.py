def LukujenErotus(ekaluku, tokaluku):
    erotus = ekaluku - tokaluku
    print(erotus)
    return erotus

summa = LukujenErotus(10,5)
print(f"Lukujen 10 ja 5 erotus on {summa}")
summa = LukujenErotus(100, 20)
print(f"Lukujen 100 ja 20 erotus on {summa}")
summa = LukujenErotus(23548, 1239)
print(f"Lukujen 23548 ja 1239 erotus on {summa}")